package operator;

public class IncAndDec {

	public static void main(String[] args) {
		int a =10;
		int b=5;
		System.out.println("\n Value of a " +a);
		System.out.println("\n Value of b" +b);
		
		System.out.println("\n Value of ++a = " +(++a));
		System.out.println("\n Value of a++ = " +a++);
		System.out.println("\n Value of --b = " +(--b));
		System.out.println("\n Value of b-- = " +b--);

	}

}
